def write(path: str, body: str) -> None:
    target = OUT / path
    target.parent.mkdir(parents=True, exist_ok=True)
    target.write_text(dedent(body).strip() + "\n", encoding="utf-8")


def load_csv(name: str) -> list[dict[str, str]]:
    with (OUT / "data" / name).open(encoding="utf-8") as f:
        return list(csv.DictReader(f))


def build_holdout_prompts(holdout: list[dict[str, str]]) -> list[dict[str, object]]:
    prompts = []
    for i, row in enumerate(holdout, start=1):
        prompts.append(
            {
                "id": f"h{i:02d}",
                "title": row["title"],
                "date": row["date"][:10],
                "article_type": row["article_type"],
                "input_materials": f"只给题目、对象和公开事实方向，不给原文：围绕“{row['title']}”写一篇半佛公开文章写法参考稿。",
                "forbidden": [
                    "不得复制或改写 holdout 原文句子",
                    "不得声称半佛本人创作",
                    "不得补未给出的数据、采访、经历",
                ],
                "scoring_focus": [
                    "标题强判断但不硬蹭",
                    "开头能用场景或反常识迅速入题",
                    "正文能从热闹拆到机制",
                    "结尾回到普通人处境或行动边界",
                ],
            }
        )
    return prompts


